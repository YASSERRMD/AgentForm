export function generateIndexFile(workflowFunctionNames: readonly string[]): string {
  if (workflowFunctionNames.length === 0) {
    return "export {};\n";
  }
  const [first] = workflowFunctionNames;
  return [
    `export * from './workflows/index.js';`,
    '',
    'async function main() {',
    `  const input = process.argv.slice(2).join(' ') || 'Hello!';`,
    `  const { ${first} } = await import('./workflows/index.js');`,
    `  const result = await ${first}(input);`,
    '  console.log(result.finalOutput);',
    '}',
    '',
    'if (import.meta.url === `file://${process.argv[1]}`) {',
    '  main();',
    '}',
    '',
  ].join('\n');
}

export function generateWorkflowsIndexFile(workflowIds: readonly string[]): string {
  const exports = workflowIds.map((id) => `export * from './${id}.js';`);
  return `${exports.join('\n')}\n`;
}

