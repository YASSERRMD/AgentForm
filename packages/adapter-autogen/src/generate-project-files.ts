interface WorkflowSummary {
  readonly id: string;
  readonly isSingleAgent: boolean;
}

/** Runs the first declared workflow (mirrors every other adapter's `generateMainFile`/`generateIndexFile`, which picks `[first]` from the workflow list the same way) — calling either its plain `run(task)` or `build_team()`, whichever `generate-workflow.ts` emitted for it. */
export function generateMainFile(workflows: readonly WorkflowSummary[]): string {
  const [first] = workflows;
  if (first === undefined) {
    return [
      '"""No workflows are declared in this application."""',
      '',
      'def main() -> None:',
      '    raise NotImplementedError("No workflows are declared in this application.")',
      '',
      '',
      'if __name__ == "__main__":',
      '    main()',
      '',
    ].join('\n');
  }

  const moduleName = toIdentifier(first.id);
  if (first.isSingleAgent) {
    return [
      'import asyncio',
      '',
      `from .workflows.${moduleName} import run`,
      '',
      '',
      'async def main() -> None:',
      '    result = await run(task="Hello!")',
      '    print(result)',
      '',
      '',
      'if __name__ == "__main__":',
      '    asyncio.run(main())',
      '',
    ].join('\n');
  }

  return [
    'import asyncio',
    '',
    `from .workflows.${moduleName} import build_team`,
    '',
    '',
    'async def main() -> None:',
    '    team = build_team()',
    '    result = await team.run(task="Hello!")',
    '    print(result.messages[-1].content)',
    '',
    '',
    'if __name__ == "__main__":',
    '    asyncio.run(main())',
    '',
  ].join('\n');
}
