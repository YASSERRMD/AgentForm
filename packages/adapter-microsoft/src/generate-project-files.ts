import { toPascalCase } from '@agentform/compiler';
import type { AgentformIR } from '@agentform/ir';
import {
  DOTNET_TARGET_FRAMEWORK,
  MICROSOFT_AGENTS_AI_OPENAI_VERSION,
  MICROSOFT_AGENTS_AI_VERSION,
  MICROSOFT_AGENTS_AI_WORKFLOWS_VERSION,
  OPENAI_DOTNET_VERSION,
} from './versions.js';

/** A real, buildable `.csproj` — verified end-to-end via a real `dotnet build` against these exact package versions on the installed net10.0 SDK. Includes `Microsoft.Agents.AI.OpenAI`/`OpenAI` proactively (mirroring `@agentform/adapter-autogen`'s `autogen-ext[openai]` inclusion) so the model-client stub's TODO comment can point at an already-referenced package. */
export function generateCsproj(ir: AgentformIR): string {
  return [
    '<Project Sdk="Microsoft.NET.Sdk">',
    '',
    '  <PropertyGroup>',
    `    <AssemblyName>${toPascalCase(ir.application.name)}</AssemblyName>`,
    '    <OutputType>Exe</OutputType>',
    `    <TargetFramework>${DOTNET_TARGET_FRAMEWORK}</TargetFramework>`,
    '    <ImplicitUsings>enable</ImplicitUsings>',
    '    <Nullable>enable</Nullable>',
    '  </PropertyGroup>',
    '',
    '  <ItemGroup>',
    `    <PackageReference Include="Microsoft.Agents.AI" Version="${MICROSOFT_AGENTS_AI_VERSION}" />`,
    `    <PackageReference Include="Microsoft.Agents.AI.OpenAI" Version="${MICROSOFT_AGENTS_AI_OPENAI_VERSION}" />`,
    `    <PackageReference Include="Microsoft.Agents.AI.Workflows" Version="${MICROSOFT_AGENTS_AI_WORKFLOWS_VERSION}" />`,
    `    <PackageReference Include="OpenAI" Version="${OPENAI_DOTNET_VERSION}" />`,
    '  </ItemGroup>',
    '',
    '</Project>',
    '',
  ].join('\n');
}

/** Same reasoning as every other adapter: `model.provider` is free-form, so there's no single fixed credential env var to name without guessing — documents the declared models as a starting point, plus the one credential the model-client stub's own TODO comment can name with confidence (`OPENAI_API_KEY`, matching its suggested `OpenAIClient` snippet). */
export function generateEnvExample(ir: AgentformIR): string {
  const lines = [
    '# The model-client stub in Models/ suggests OPENAI_API_KEY as a starting',
    'point (Microsoft.Agents.AI.OpenAI is already referenced in the .csproj)',
    '— set whichever credential env var your chosen provider actually needs.',
  ];
  const modelLines = [...ir.models.entries()].map(
    ([id, model]) => `# ${id}: provider=${model.provider} model=${model.model}`,
  );
  return [
    ...lines.map((line) => `# ${line}`),
    '#',
    'OPENAI_API_KEY=',
    '#',
    '# Declared models in this application:',
    ...modelLines,
    '',
  ].join('\n');
}

