import { toIdentifier } from '@agentform/compiler';
import type { AgentformIR } from '@agentform/ir';
import { CREWAI_VERSION, PYTHON_VERSION_REQUIREMENT } from './versions.js';

/** A real, installable `pyproject.toml` — same verified `hatchling` + `packages = ["src"]` pattern as every other Python adapter in this repo. */
export function generatePyprojectToml(ir: AgentformIR): string {
  return [
    '[project]',
    `name = ${JSON.stringify(ir.application.name)}`,
    `version = ${JSON.stringify(ir.application.version)}`,
    `requires-python = ${JSON.stringify(PYTHON_VERSION_REQUIREMENT)}`,
    'dependencies = [',
    `    ${JSON.stringify(`crewai==${CREWAI_VERSION}`)},`,
    ']',
    '',
    '[build-system]',
    'requires = ["hatchling"]',
    'build-backend = "hatchling.build"',
    '',
    '[tool.hatch.build.targets.wheel]',
    'packages = ["src"]',
    '',
  ].join('\n');
}

/**
 * `OPENAI_API_KEY` and `GOOGLE_API_KEY`/`GEMINI_API_KEY` are named
 * explicitly — verified directly against the installed package's own
 * provider integrations (`crewai/llms/providers/openai/completion.py` and
 * `.../gemini/completion.py` both read these exact names via `os.getenv`).
 * Every other provider is free-form in Agentform's schema, so there's no
 * single fixed credential env var to name without guessing — documents the
 * declared models as a starting point instead.
 */
export function generateEnvExample(ir: AgentformIR): string {
  const lines = [
    '# CrewAI resolves each agent\'s llm= "<provider>/<model>" string against',
    'its own list of native provider integrations, falling back to LiteLLM',
    'when installed. The two natively-verified providers below read these',
    'exact environment variable names automatically:',
    '',
    '# For OpenAI models:',
  ];
  const modelLines = [...ir.models.entries()].map(
    ([id, model]) => `# ${id}: provider=${model.provider} model=${model.model}`,
  );
  return [
    ...lines.map((line) => (line.length > 0 ? `# ${line}` : '#')),
    'OPENAI_API_KEY=',
    '#',
    '# For Google/Gemini models (either name works):',
    'GOOGLE_API_KEY=',
    'GEMINI_API_KEY=',
    '#',
    '# Declared models in this application:',
    ...modelLines,
    '',
  ].join('\n');
}

