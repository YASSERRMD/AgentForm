interface WorkflowSummary {
  readonly id: string;
}

/** Runs the first declared workflow's root agent through a real `Runner` + `InMemorySessionService` — verified end-to-end against the installed package, including the exact async `create_session`/`run_async`/event-streaming shape. */
export function generateMainFile(workflows: readonly WorkflowSummary[]): string {
  const [first] = workflows;
  if (first === undefined) {
    return [
      '"""No workflows are declared in this application."""',
      '',
      'def main() -> None:',
      '    raise NotImplementedError("No workflows are declared in this application.")',
      '',
      '',
      'if __name__ == "__main__":',
      '    main()',
      '',
    ].join('\n');
  }

  const moduleName = toIdentifier(first.id);
  return [
    'import asyncio',
    '',
    'from google.adk.runners import Runner',
    'from google.adk.sessions import InMemorySessionService',
    'from google.genai import types',
    '',
    `from .workflows.${moduleName} import build_root_agent`,
    '',
    'APP_NAME = "agentform-app"',
    '',
    '',
    'async def main() -> None:',
    '    session_service = InMemorySessionService()',
    '    session = await session_service.create_session(app_name=APP_NAME, user_id="user")',
    '    runner = Runner(app_name=APP_NAME, agent=build_root_agent(), session_service=session_service)',
    '',
    '    message = types.Content(role="user", parts=[types.Part.from_text(text="Hello!")])',
    '    async for event in runner.run_async(user_id="user", session_id=session.id, new_message=message):',
    '        if event.is_final_response() and event.content and event.content.parts:',
    '            for part in event.content.parts:',
    '                if part.text:',
    '                    print(part.text)',
    '',
    '',
    'if __name__ == "__main__":',
    '    asyncio.run(main())',
    '',
  ].join('\n');
}
