import { toIdentifier } from '@agentform/compiler';
import type { AgentformIR } from '@agentform/ir';
import {
  AUTOGEN_AGENTCHAT_VERSION,
  AUTOGEN_EXT_VERSION,
  PYTHON_VERSION_REQUIREMENT,
} from './versions.js';

/**
 * A real, installable `pyproject.toml` — same verified `hatchling` +
 * `packages = ["src"]` pattern as `@agentform/adapter-langgraph` (real
 * `pip install .` confirmed there against the identical `src`-named-package
 * layout). Pins `autogen-ext[openai]` specifically: the `openai` extra is
 * what makes `autogen_ext.models.openai` importable, the most common
 * starting point referenced in the generated model-client stub's docstring
 * — a real provider may need a different extra (`anthropic`, `azure`,
 * `ollama`, ...), left for the user to adjust once they fill in the stub.
 */
export function generatePyprojectToml(ir: AgentformIR): string {
  return [
    '[project]',
    `name = ${JSON.stringify(ir.application.name)}`,
    `version = ${JSON.stringify(ir.application.version)}`,
    `requires-python = ${JSON.stringify(PYTHON_VERSION_REQUIREMENT)}`,
    'dependencies = [',
    `    ${JSON.stringify(`autogen-agentchat==${AUTOGEN_AGENTCHAT_VERSION}`)},`,
    `    ${JSON.stringify(`autogen-ext[openai]==${AUTOGEN_EXT_VERSION}`)},`,
    ']',
    '',
    '[build-system]',
    'requires = ["hatchling"]',
    'build-backend = "hatchling.build"',
    '',
    '[tool.hatch.build.targets.wheel]',
    'packages = ["src"]',
    '',
  ].join('\n');
}

/** Same reasoning as every other Python adapter: `model.provider` is free-form, so there's no single fixed credential env var to name without guessing — this documents the declared models as a starting point instead. */
export function generateEnvExample(ir: AgentformIR): string {
  const lines = [
    '# Agent model clients are generated as stubs (see src/models/) — wire up',
    'your model provider(s) of choice and set their credentials here. Nothing',
    'in generated code reads these automatically; this file is a starting point.',
  ].map((line) => `# ${line}`);
  const modelLines = [...ir.models.entries()].map(
    ([id, model]) => `# ${id}: provider=${model.provider} model=${model.model}`,
  );
  return [...lines, '#', ...modelLines, ''].join('\n');
}

