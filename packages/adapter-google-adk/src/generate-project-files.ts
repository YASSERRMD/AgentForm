import { toIdentifier } from '@agentform/compiler';
import type { AgentformIR } from '@agentform/ir';
import { GOOGLE_ADK_VERSION, PYTHON_VERSION_REQUIREMENT } from './versions.js';

/** A real, installable `pyproject.toml` — same verified `hatchling` + `packages = ["src"]` pattern as `@agentform/adapter-langgraph`/`@agentform/adapter-autogen`. */
export function generatePyprojectToml(ir: AgentformIR): string {
  return [
    '[project]',
    `name = ${JSON.stringify(ir.application.name)}`,
    `version = ${JSON.stringify(ir.application.version)}`,
    `requires-python = ${JSON.stringify(PYTHON_VERSION_REQUIREMENT)}`,
    'dependencies = [',
    `    ${JSON.stringify(`google-adk==${GOOGLE_ADK_VERSION}`)},`,
    ']',
    '',
    '[build-system]',
    'requires = ["hatchling"]',
    'build-backend = "hatchling.build"',
    '',
    '[tool.hatch.build.targets.wheel]',
    'packages = ["src"]',
    '',
  ].join('\n');
}

/** Same reasoning as every other Python adapter: `model.provider` is free-form, so there's no single fixed credential env var to name without guessing — documents declared models instead. ADK's own Gemini path reads `GOOGLE_API_KEY`/`GOOGLE_GENAI_USE_VERTEXAI` by convention, called out explicitly since it's the one credential this adapter's own model-string generation can name with confidence. */
export function generateEnvExample(ir: AgentformIR): string {
  const lines = [
    '# For Google/Gemini models (the ADK SDK reads this automatically):',
    'GOOGLE_API_KEY=',
    '#',
    '# Non-Gemini providers need a real BaseLlm instance instead of a plain',
    "# model string — see the TODO comment in that agent's generated file, and",
    '# set whichever credential env var that provider expects here.',
  ];
  const modelLines = [...ir.models.entries()].map(
    ([id, model]) => `# ${id}: provider=${model.provider} model=${model.model}`,
  );
  return [...lines, '#', ...modelLines, ''].join('\n');
}

