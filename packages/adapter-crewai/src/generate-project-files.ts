interface WorkflowSummary {
  readonly id: string;
}

/** Runs the first declared workflow's crew — `Crew.kickoff()` is synchronous (verified: its real signature has no `async`), unlike `@agentform/adapter-autogen`/`@agentform/adapter-google-adk`'s `asyncio`-based entrypoints. */
export function generateMainFile(workflows: readonly WorkflowSummary[]): string {
  const [first] = workflows;
  if (first === undefined) {
    return [
      '"""No workflows are declared in this application."""',
      '',
      'def main() -> None:',
      '    raise NotImplementedError("No workflows are declared in this application.")',
      '',
      '',
      'if __name__ == "__main__":',
      '    main()',
      '',
    ].join('\n');
  }

  const moduleName = toIdentifier(first.id);
  return [
    `from .workflows.${moduleName} import build_crew`,
    '',
    '',
    'def main() -> None:',
    '    crew = build_crew()',
    '    result = crew.kickoff()',
    '    print(result.raw)',
    '',
    '',
    'if __name__ == "__main__":',
    '    main()',
    '',
  ].join('\n');
}
