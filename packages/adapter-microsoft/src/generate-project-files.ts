interface WorkflowSummary {
  readonly id: string;
  readonly isSingleAgent: boolean;
}

/** Runs the first declared workflow — a plain `agent.RunAsync(...)` for a single-participant workflow, or the verified real `InProcessExecution.RunAsync(workflow, input)` + `WorkflowEvent`/`ExecutorCompletedEvent` streaming pattern otherwise. Top-level-statements style, matching the framework's own sample `Program.cs` shape. */
export function generateProgramFile(workflows: readonly WorkflowSummary[]): string {
  const [first] = workflows;
  if (first === undefined) {
    return [
      'throw new NotImplementedException("No workflows are declared in this application.");',
      '',
    ].join('\n');
  }

  const className = `${toPascalCase(first.id)}Workflow`;
  if (first.isSingleAgent) {
    return [
      'using GeneratedApp.Workflows;',
      'using Microsoft.Agents.AI;',
      '',
      `AIAgent agent = ${className}.Build();`,
      'Console.WriteLine(await agent.RunAsync("Hello!"));',
      '',
    ].join('\n');
  }

  return [
    'using GeneratedApp.Workflows;',
    'using Microsoft.Agents.AI.Workflows;',
    '',
    `Workflow workflow = ${className}.Build();`,
    'await using Run run = await InProcessExecution.RunAsync(workflow, "Hello!");',
    'foreach (WorkflowEvent evt in run.NewEvents)',
    '{',
    '    if (evt is ExecutorCompletedEvent completed)',
    '    {',
    '        Console.WriteLine($"{completed.ExecutorId}: {completed.Data}");',
    '    }',
    '}',
    '',
  ].join('\n');
}
