import type { AgentformIR } from '@agentform/ir';
import {
  NODE_ENGINE_RANGE,
  OPENAI_AGENTS_SDK_VERSION,
  TYPES_NODE_VERSION,
  TYPESCRIPT_VERSION,
  ZOD_VERSION,
} from './versions.js';

export function generatePackageJson(ir: AgentformIR): string {
  const packageJson = {
    name: `${ir.application.name}-openai`,
    version: ir.application.version,
    private: true,
    type: 'module',
    engines: { node: NODE_ENGINE_RANGE },
    scripts: {
      build: 'tsc',
      start: 'node dist/index.js',
    },
    dependencies: {
      '@openai/agents': OPENAI_AGENTS_SDK_VERSION,
      zod: ZOD_VERSION,
    },
    devDependencies: {
      typescript: TYPESCRIPT_VERSION,
      '@types/node': TYPES_NODE_VERSION,
    },
  };
  return `${JSON.stringify(packageJson, null, 2)}\n`;
}

export function generateTsconfig(): string {
  const tsconfig = {
    compilerOptions: {
      target: 'ES2022',
      module: 'NodeNext',
      moduleResolution: 'NodeNext',
      outDir: 'dist',
      rootDir: 'src',
      strict: true,
      esModuleInterop: true,
      skipLibCheck: true,
      declaration: true,
    },
    include: ['src'],
  };
  return `${JSON.stringify(tsconfig, null, 2)}\n`;
}

/** Documents the env vars the OpenAI Agents SDK itself expects — never the resolved value of anything from the specification (§22 "avoid secret values, use environment-variable references"). `OPENAI_API_KEY` is what the SDK's own OpenAI client reads by default; nothing in generated code hardcodes it. */
export function generateEnvExample(): string {
  return ['# The OpenAI Agents SDK reads this automatically.', 'OPENAI_API_KEY=', ''].join('\n');
}

